let englishTexts = {};

let currentLang = "en";

function saveEnglishTexts() {
  document.querySelectorAll("[data-lang]").forEach(el => {
    englishTexts[el.getAttribute("data-lang")] = el.textContent;
  });
}

function switchLanguage() {
  if (currentLang === "en") {
    currentLang = "ar";
    document.body.dir = "rtl";
    document.getElementById("lang-toggle").textContent = "English";

    document.querySelectorAll("[data-lang]").forEach(el => {
      const key = el.getAttribute("data-lang");
      if (translations.ar[key]) {
        el.textContent = translations.ar[key];
      }
    });
  } else {
    currentLang = "en";
    document.body.dir = "ltr";
    document.getElementById("lang-toggle").textContent = "عربي";

    document.querySelectorAll("[data-lang]").forEach(el => {
      const key = el.getAttribute("data-lang");
      el.textContent = englishTexts[key];
    });
  }
}

window.onload = () => {
  saveEnglishTexts();
  document.getElementById("lang-toggle").addEventListener("click", switchLanguage);
};

